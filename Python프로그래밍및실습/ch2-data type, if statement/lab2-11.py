# 형제 자매 찾기 문제


child1 = input("성별 1: ")
child2 = input("성별 2: ")

if child1 == "남자" and child2 == "남자" :
    print("형제입니다.")
elif child1 == "남자" and child2 == "여자" :
    print("남매입니다.")
elif child1 == "여자" and child2 == "남자" :
    print("남매입니다.")
else :
    print("자매입니다.")

    
        










